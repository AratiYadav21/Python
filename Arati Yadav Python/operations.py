# Demonstrate the use of all operations on list.
L1=[1,2,4,3,6,5,7]
L2=[3,5,7,3,8,9,0]
print(L1)
print(L2)

print (L1*2)   #Repetation

print (L1+L2)    # Concatenation

print (2 in L1)   # Membership

for i in L1:      # Iteration
    print(i)
    
print (len(L2))   # Length

